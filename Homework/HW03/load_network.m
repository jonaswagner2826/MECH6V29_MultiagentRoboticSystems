function[X,n,N]=load_network(network_no,Delta);
n=2; X=[];
if (network_no==1);
  N=8;  
  Delta=1.001*Delta;
  X(:,1)=[-Delta;0]; X(:,2)=[-Delta;Delta/3];
  X(:,3)=[-Delta;-Delta/3]; X(:,4)=[-Delta/3;0];
  X(:,5)=[Delta/3;0]; X(:,6)=[Delta;Delta/3];
  X(:,7)=[Delta;0]; X(:,8)=[Delta;-Delta/3];
elseif (network_no==2);
  N=9;
  Delta=1.5*Delta;
  X(:,1)=[-2*Delta/3;0]; X(:,2)=[2*Delta/3;0];
  X(:,3)=[-Delta/2;Delta/2]; X(:,4)=[Delta/2;Delta/2];
  X(:,5)=[-Delta/2;-Delta/2]; X(:,6)=[Delta/2;-Delta/2];
  X(:,7)=[0;2*Delta/3]; X(:,8)=[0;-2*Delta/3]; X(:,9)=[0;0];
else;
  N=7;
  Delta=1*Delta;
  X(:,1)=[-8*Delta/5;0]; X(:,2)=[-4*Delta/5;0];
  X(:,3)=[0;0]; X(:,4)=[0;4*Delta/5]; X(:,5)=[0;8*Delta/5];
  X(:,6)=[4*Delta/5;0]; X(:,7)=[8*Delta/5;0];
end;
